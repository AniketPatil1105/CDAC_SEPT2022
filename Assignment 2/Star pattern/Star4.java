class Star4{
	public static void main(String[] args){
		
	for(int i=1;i<=5;i++){
		
		for(int j=1;j<i;j++){
			
			System.out.print(" ");			//printing space
			
		}
		
		
		for(int j=5;j>=i;j--){
			
			System.out.print("*");			//printing value
			
			
		}
		
		System.out.println();		
	}
	
	}
	
}